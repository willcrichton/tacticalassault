-------------------------------------------DAMAGE
function ENT:OnTakeDamage(dmg)

	local Damage = 	0

	if dmg:IsExplosionDamage() then
		Damage = dmg:GetDamage()
	else
		Damage = (dmg:GetDamage()) / 4
	end

	self.JetHealth = self.JetHealth - Damage

end
